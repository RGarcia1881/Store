package com.store;

import java.io.Serializable;

public class DataRow implements Serializable {
    private int id;
    private String name;
    private int price, stock;
    // Otros atributos

    public DataRow(int id, String name, int price, int stock) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    @Override
    public String toString() {
        return "DataRow{id=" + id + ", name='" + name + '\'' + '}';
    }
}
